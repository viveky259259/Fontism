ThemeData getTheme({TextTheme? theme, required bool isLight}) {
  if (theme == null) theme = GoogleFonts.nunitoTextTheme();
  return new ThemeData(
      brightness: isLight ? Brightness.light : Brightness.dark,
      scaffoldBackgroundColor: Colors.red.shade400,
      textTheme: theme);
}

class MyApp extends StatelessWidget {
  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
        home: DynamicTheme(
      data: (Brightness brightness) =>
          getTheme(isLight: brightness == Brightness.light),
      themedWidgetBuilder: (context, data) {
        return ResponsiveWrapper(
          shrinkWrap: true,
          mediaQueryData: MediaQuery.of(context),
          minWidth: 400,
          child: MaterialApp(
            theme: data,
            home: MyHomePage(title: 'Fontism'),
          ),
          breakpoints: [
            ResponsiveBreakpoint.resize(480, name: MOBILE),
            ResponsiveBreakpoint.autoScale(800, name: TABLET),
            ResponsiveBreakpoint.resize(1000, name: DESKTOP),
          ],
        );
      },
    ));
  }
}

