class _MyHomePageState extends State<MyHomePage> {
  bool isLight = true;

  void updateFont(TextTheme theme) {
    DynamicTheme.of(context)!
        .setThemeData(getTheme(theme: theme, isLight: isLight));
  }

  Widget drawerWidget() {
    return Drawer(child: Builder(builder: (context) {
      return ListView.builder(
          itemCount: themes.length,
          itemBuilder: (context, index) {
            String key = themes.keys.elementAt(index);
            TextTheme theme = themes[key]!();
            return Theme(
              data: getTheme(theme: theme, isLight: isLight),
              child: ListTile(
                  key: UniqueKey(),
                  onTap: () {
                    updateFont(theme);
                  },
                  title: Text(key)),
            );
          });
    }));
  }

  Widget bodyWidget() {
    return ViewWidget();
